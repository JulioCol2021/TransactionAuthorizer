package transaction;

public class Balance {
    private double food = 100.0;
    private double meal = 100.0;
    private double cash = 100.0;

    // Getters e setters
    public double getFood() {
        return food;
    }

    public void setFood(double food) {
        this.food = food;
    }

    public double getMeal() {
        return meal;
    }

    public void setMeal(double meal) {
        this.meal = meal;
    }

    public double getCash() {
        return cash;
    }

    public void setCash(double cash) {
        this.cash = cash;
    }
}
